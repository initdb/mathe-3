# Rechen mit Vektoren in R
a <- c(1,2,3)

a * a   # element multiplication
        # 1 * 1 = 1
        # 2 * 2 = 4
        # 3 * 3 = 9

a * 5   # multiply each component with 5

a^2     # quadruple each element

a + 1   # icrement each element by 1

a %*% a # matrix multiplaction aka. scalar product
